<template>
  <!-- 仅传递文字一般不用插槽 -->
  <div class="tab-control">
    <div v-for="(item,index) in titles" class="tab-control-item"  :class="{active:index === current}" @click="itemClick(index)" :key="index">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
export default {
  name: "TabControl",
  props: {
    titles: {
      type:  Array,
      default() {
        return []
      }
    }
  },
  data() {
    return {
      current: 0
    }
  },
  methods: {
    itemClick(index) {
      this.current = index;
      // 事件点击传出
      this.$emit('tabClick', index)
    }
  }
}
</script>

